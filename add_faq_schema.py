def build_faq_schema(faqs):
    entities = [
        {"@type": "Question",
         "name": faq["q"],
         "acceptedAnswer": {"@type": "Answer", "text": faq["a"]}}
        for faq in faqs
    ]
    schema = {
        "@context": "https://schema.org",
        "@type": "FAQPage",
        "mainEntity": entities
    }
    return f'<script type="application/ld+json">\n{json.dumps(schema, ensure_ascii=False, indent=2)}\n</script>'


def build_faq_html(faqs):
    items = ""
    for faq in faqs:
        items += f"""  <div class="faq-item">
    <div class="faq-q">{faq["q"]}</div>
    <div class="faq-a">{faq["a"]}</div>
  </div>\n"""

    return f"""
<style>
.faq-section {{ background:#fff; border:2px solid var(--primary,#c0392b); border-radius:10px; padding:28px; margin:40px 0; }}
.faq-section h2 {{ font-size:1.1rem; font-weight:700; color:var(--primary,#c0392b); margin-bottom:20px; }}
.faq-item {{ border-bottom:1px solid #eee; padding:16px 0; }}
.faq-item:last-child {{ border-bottom:none; }}
.faq-q {{ font-weight:700; font-size:0.95rem; margin-bottom:8px; }}
.faq-q::before {{ content:"Q. "; color:var(--primary,#c0392b); }}
.faq-a {{ font-size:0.9rem; line-height:1.7; color:#444; }}
.faq-a::before {{ content:"A. "; font-weight:700; color:#333; }}
</style>
<div class="faq-section">
  <h2>よくある質問</h2>
{items}</div>
"""


def add_faq_to_file(filepath, faqs):
    with open(filepath, "r", encoding="utf-8") as f:
        html = f.read()

    if "FAQPage" in html:
        print(f"SKIP: {os.path.basename(filepath)}")
        return

    # JSON-LDを</head>直前に挿入
    schema_tag = build_faq_schema(faqs)
    html = html.replace("</head>", schema_tag + "\n</head>")

    # FAQのHTMLブロックをフッター直前に挿入
    faq_html = build_faq_html(faqs)
    html = html.replace("<div class=\"related-articles\"", faq_html + "\n<div class=\"related-articles\"")

    with open(filepath, "w", encoding="utf-8") as f:
        f.write(html)

    print(f"OK: {os.path.basename(filepath)}")


